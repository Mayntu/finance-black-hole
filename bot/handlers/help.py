import structlog
from aiogram import F, Router
from aiogram.filters import Command
from aiogram.types import Message

logger = structlog.get_logger(__name__)
router = Router()

HELP_TEXT = """\
📖 *Что умеет FinanceBlackHole*

*Как пользоваться*
Просто напиши трату текстом, голосом или пришли фото чека — бот разберёт сумму и категорию.

*Кнопки внизу*
• *📊 Сегодня* — траты за сегодня и дневной остаток от бюджета
• *📈 Неделя* — неделя + топ категорий
• *🌐 Дашборд* — мини-приложение: графики, дашборд (если настроен HTTPS)
• *🌐 Цели* — то же мини-приложение, вкладка целей
• *🎯 Цели* — цели текстом в чате (прогресс без веба)
• *🎖 Миссии* — персональные челленджи
• *🧠 Инсайт* — «финансовое зеркало» по последним 7 дням
• *📂 Категории* — свои категории для AI
• *❓ Помощь* — это сообщение
• *👤 Профиль* — уровень, стрик, ачивки + кнопка веб-дашборда
• *💳 Бюджет* — месячный лимит: `/budget 200000`

*Команды*
/start — регистрация
/help — это же меню текстом
/goals — цели списком в чате
/goal — новая цель
/save N — отложить N ₸ на цель
/missions — миссии
/categories — категории
/insight — AI-инсайт за 7 дней
/today, /week — статистика
/dashboard — ссылки на мини-приложение

*Синяя кнопка «Дашборд» у поля ввода* (если есть) — открывает веб; там внизу вкладки *Цели*, *История*, *Профиль*. На *Истории* у каждой траты есть «Изменить» / «Удалить».
"""


@router.message(Command("help"))
@router.message(F.text == "❓ Помощь")
async def cmd_help(message: Message) -> None:
    await message.answer(HELP_TEXT, parse_mode="Markdown")
